    //Funcion que devuelve el alfabeto
    public String alfabeto2cadena() {
        String alfcad = "";
        for (int i = 0; i < modeloList.getSize(); i++) {
            alfcad += modeloList.get(i).toString();
        }
        return alfcad;
    }

//<-------------------------------------------------------------------------------------------------------------------------------->
    //Funcion para evitar simbolos repetidos
    public int buscar(String elemento) {
        for (int i = 0; i < modeloList.getSize(); i++) {
            if (elemento.equals(modeloList.get(i).toString())) {
                return i;
            }
        }
        return -1;
    }

