    private void agregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_agregarActionPerformed
        // TODO add your handling code here:
        String elemento = insimbolo.getText().toLowerCase().trim();

        if (buscar(elemento) == -1) {
            if (elemento.matches("[a-z]|[0-9]")) {
                modeloList.addElement(elemento);
                entradasimbolos.setModel(modeloList);
                insimbolo.setText("");
            } else {
                JOptionPane.showMessageDialog(this, "Debe ingresar una solo letra o número", "Error", 0);
            }
        } else {
            JOptionPane.showMessageDialog(this, "Ya hay un elemento igual dentro del alfabeto", "Error", 0);
        }
    }//GEN-LAST:event_agregarActionPerformed

    private void eliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_eliminarActionPerformed
        // TODO add your handling code here:
        if (entradasimbolos.getSelectedIndex() != -1) {
            int idxSel = entradasimbolos.getSelectedIndex();
            modeloList.remove(idxSel);
            entradasimbolos.setModel(modeloList);
        } else {
            JOptionPane.showMessageDialog(this, "Debe seleccionar un item del alfabeto", "Error", 0);
        }
    }//GEN-LAST:event_eliminarActionPerformed

